    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed

        // Get the table model and clear existing rows
        var model = (javax.swing.table.DefaultTableModel) jTableResults.getModel();
        model.setRowCount(0);

        // Get the selected object from the ComboBox
        Object selectedItem = jComboBoxClass.getSelectedItem();

        // Validation: Check if something is selected and if it's an instance of Student class
        if (selectedItem == null || !(selectedItem instanceof ClassGroup)) {
            JOptionPane.showMessageDialog(this, "Please select a valid Course and Class.");
            return;
        }

        // Cast the selected item to ClassGroup object to access its methods
        ClassGroup selectedClassGroup = (ClassGroup) selectedItem;
        int classGroupCode = selectedClassGroup.getClassGroupCode();
        jLabelCourse.setText("Course: " + selectedClassGroup.getCourseName() +" "+ selectedClassGroup.getQqiCourseCode());
        jLabelClassGroup.setText("Class group" +": "+ selectedClassGroup.getClassGroupCode());
        // Prepare SQL Query
        String sql = """
        SELECT 
            m.module_name, 
            CONCAT(s.first_name, ' ', s.surname) AS student_name, 
            a.result
        FROM student s
        JOIN class_group cg ON s.class_group_code = cg.class_group_code
        JOIN course_has_module chm ON cg.course_id = chm.course_id
        JOIN module m ON chm.qqi_module_code = m.qqi_module_code
        LEFT JOIN assessment a ON (s.student_id = a.student_id AND m.qqi_module_code = a.qqi_module_code)
        WHERE s.class_group_code = ?
        ORDER BY m.module_name ASC, s.first_name ASC;
    """;

        try (Connection con = DriverManager.getConnection(URL, USER, PASSWORD); PreparedStatement ps = con.prepareStatement(sql)) {

            ps.setInt(1, classGroupCode);

            try (ResultSet rs = ps.executeQuery()) {
                boolean found = false;
                while (rs.next()) {
                    found = true;
                    String module = rs.getString("module_name");
                    String student = rs.getString("student_name");
                    
                    // Use getObject for result to handle NULL values
                    Object result = rs.getObject("result");

                    model.addRow(new Object[]{
                        module,
                        student,
                        (result == null ? "N/A" : result)
                    });
                }

                if (!found) {
                    JOptionPane.showMessageDialog(this, "No students or grades found for this class group.");
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        }


    }//GEN-LAST:event_jButtonSearchActionPerformed

