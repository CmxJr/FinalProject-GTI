    private void FillComboBox() {

        var model = (javax.swing.table.DefaultTableModel) jTableResults.getModel();
        model.setRowCount(0);

        String sql = """
                     SELECT c.course_name, c.qqi_course_code, cg.class_group_code 
                     FROM course c 
                     JOIN class_group cg ON c.course_id = cg.course_id 
                     ORDER BY c.course_name ASC
                    """;

        try (Connection con = DriverManager.getConnection(URL, USER, PASSWORD); PreparedStatement ps = con.prepareStatement(sql); ResultSet rs = ps.executeQuery()) {

            jComboBoxClass.removeAllItems();

            while (rs.next()) {
                // Create the object with data from database
                ClassGroup cg = new ClassGroup(
                        rs.getInt("class_group_code"),
                        rs.getString("course_name"),
                        rs.getString("qqi_course_code")
                );

                // Add the whole object to the ComboBox
                jComboBoxClass.addItem(cg);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading classes: " + e.getMessage());
        }
        jComboBoxClass.setSelectedIndex(-1);

    }

