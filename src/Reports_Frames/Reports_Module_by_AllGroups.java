    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        // 1. Get and clean the module name
        String selectedModule = jComboBoxModule.getSelectedItem().toString().trim();
        String moduleNameOnly = selectedModule.substring(0, selectedModule.length() - 6).trim();

        // 2. Clear the table before filling
        var model = (javax.swing.table.DefaultTableModel) jTableResult.getModel();
        model.setRowCount(0);

        try {
            String sql = """
                 SELECT 
                     s.first_name, 
                     s.surname, 
                     a.assignment_1, 
                     a.assignment_2, 
                     a.exam, 
                     a.result
                 FROM student s
                 JOIN assessment a ON s.student_id = a.student_id
                 JOIN module m ON a.qqi_module_code = m.qqi_module_code
                 WHERE m.module_name = ?
                 ORDER BY first_name ASC""";

            try (Connection con = DriverManager.getConnection(URL, USER, PASSWORD); PreparedStatement ps = con.prepareStatement(sql)) {

                ps.setString(1, moduleNameOnly);

                try (ResultSet rs = ps.executeQuery()) {
                    boolean dataFound = false;

                    while (rs.next()) {
                        dataFound = true;

                        String fullName = rs.getString("first_name") + " " + rs.getString("surname");

                        // Fetch numeric values. 
                        // If the database has NULL, rs.getDouble returns 0.0 automatically.
                        double a1 = rs.getDouble("assignment_1");
                        double a2 = rs.getDouble("assignment_2");
                        double exam = rs.getDouble("exam");
                        double result = rs.getDouble("result");

                        // Add the row using double values instead of Strings like "-"
                        model.addRow(new Object[]{
                            fullName,
                            a1,
                            a2,
                            exam,
                            result
                        });
                    }

                    if (!dataFound) {
                        JOptionPane.showMessageDialog(this, "No students found for module: " + moduleNameOnly);
                    }
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        }

    }//GEN-LAST:event_jButtonSearchActionPerformed

