    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        String sql = """
                     SELECT qqi_module_code,
                     module_name
                     FROM module
                     ORDER BY module_name ASC;""";

        try (Connection con = DriverManager.getConnection(URL, USER, PASSWORD); PreparedStatement ps = con.prepareStatement(sql); ResultSet res = ps.executeQuery()) {

            while (res.next()) {
                String qqiModule = res.getString("qqi_module_code");
                String ModuleName = res.getString("module_name");

                String FullModuleName
                        = (ModuleName == null ? "" : ModuleName) + " "
                        + (qqiModule == null ? "" : qqiModule);

                FullModuleName = FullModuleName.trim().replaceAll("\\s+", " ");

                jComboBoxModule.addItem(FullModuleName);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
        jComboBoxModule.setSelectedIndex(-1);
    }//GEN-LAST:event_formWindowActivated

