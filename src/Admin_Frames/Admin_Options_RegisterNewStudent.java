    public boolean validateFields() {

        String firstName = jTextFieldFirstName.getText().trim();
        String surname = jTextFieldSurname.getText().trim();
        String addressLine1 = jTextFieldAddressLine1.getText();
        String addressLine2 = jTextFieldAddressLine2.getText();
        String email = jTextFieldEmail.getText().trim();
        String eircode = jTextFieldEircode.getText().trim().toUpperCase();
        String phone = jTextFieldPhone.getText().trim();
        String PPSN = jTextFieldPPSN.getText().trim().toUpperCase();

        if (firstName.isEmpty() || surname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "First Name and Surname are required.");
            return false;
        }

        if (addressLine1.isEmpty() || addressLine2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "A full address is required (Line 1 and 2).");
            return false;
        }

        // Validate ComboBox (Check if a county is selected)
        if (jComboBoxCounty.getSelectedIndex() <= 0) {
            JOptionPane.showMessageDialog(this, "Please select a County.");
            return false;
        }

        String emailRegex = "^[A-Za-z0-9+_.-]+@(.+)$";
        if (!email.matches(emailRegex)) {
            JOptionPane.showMessageDialog(this, "Invalid Email format.");
            return false;
        }

        // Validate Eircode (Irish Format: 7 characters, e.g., D02 X123)
        // Pattern: 1 Letter + 2 Digits/Letters + Space? + 4 Digits/Letters
        String eircodeRegex = "^[A-Z][0-9][0-9W][0-9A-Z]{4}$";
        if (eircode.equals("H91PHK1") || !eircode.matches(eircodeRegex)) {
            JOptionPane.showMessageDialog(this, "Invalid Eircode. Please use a format like A65F123.");
            return false;
        }

        // Validate Phone (Numeric, between 7 and 15 digits)
        // If you have ghost text "000 0000", check for it first
        if (phone.equals("0000000000") || !phone.replaceAll("\\s+", "").matches("\\d{7,15}")) {
            JOptionPane.showMessageDialog(this, "Invalid Phone number. Use only digits.");
            return false;
        }

        if (PPSN.isEmpty() || !PPSN.matches("^[0-9]{7}[A-Z]{1,2}$")) {
            JOptionPane.showMessageDialog(this,
                    "Invalid PPSN. It must be 7 numbers followed by 1 or 2 letters (e.g., 1234567AB).",
                    "Validation Error",
                    JOptionPane.WARNING_MESSAGE);
            return false;
        }

        if (jTextFieldDoBDay.getText().equals("XX") || jTextFieldDoBDay.getText().trim().isEmpty()
                || jTextFieldDoBMonth.getText().equals("XX") || jTextFieldDoBMonth.getText().trim().isEmpty()
                || jTextFieldDoBYear.getText().equals("XXXX") || jTextFieldDoBYear.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please fill in all Date of Birth fields.");
            return false;
        }

        if (!jTextFieldDoBDay.getText().matches("\\d+")
                || !jTextFieldDoBMonth.getText().matches("\\d+")
                || !jTextFieldDoBYear.getText().matches("\\d+")) {

            JOptionPane.showMessageDialog(this, "Date of Birth must contain only numbers.");
            return false;
        }

        return true;
    }

