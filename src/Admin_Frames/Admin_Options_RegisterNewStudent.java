    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // SQL query using INNER JOIN
        String sql = "SELECT cg.class_group_code, c.course_name, c.qqi_course_code "
                + "FROM final_project.course c "
                + "INNER JOIN final_project.class_group cg ON c.course_id = cg.course_id "
                + "ORDER BY c.course_name ASC, cg.class_group_code ASC";

        try (Connection con = DriverManager.getConnection(URL, USER, PASSWORD); PreparedStatement ps = con.prepareStatement(sql); ResultSet res = ps.executeQuery()) {

            // Clear the ComboBox before populating
            jComboBoxClasseGroup.removeAllItems();

            while (res.next()) {
                // 1. Extract data from the database
                int groupCode = res.getInt("class_group_code");
                String name = res.getString("course_name");
                String qqi = res.getString("qqi_course_code");

                String displayName = name;
                if (name != null) {
                    String[] words = name.split("\\s+"); // Split by spaces
                    if (words.length > 4) {
                        displayName = words[0] + " " + words[1] + " " + words[2] +" "+ words[3] + "...";
                    }
                }

                // 2. Create the Object (using your constructor)
                ClassGroup cg = new ClassGroup(groupCode, displayName, qqi);

                // 3. Add the WHOLE object to the ComboBox
                // Swing will automatically call cg.toString() to display it
                jComboBoxClasseGroup.addItem(cg);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        }

        // Set default selection to empty
        jComboBoxClasseGroup.setSelectedIndex(-1);
    }//GEN-LAST:event_formWindowOpened

