    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        if (currentSelectedStaff == null) {
            showError("Please select a staff member first.");
            return;
        }

        // String Confirmation message
        String message = "Are you sure you want to delete " + currentSelectedStaff.getFullName() + "?";
        String title = "Confirm Deletion";

        // PopUp
        int response = JOptionPane.showConfirmDialog(null, message, title, JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        // If yes
        if (response == JOptionPane.YES_OPTION) {

            String sql = "DELETE FROM staff WHERE staff_id = ?";

            try (Connection con = DriverManager.getConnection(URL, USER, PASSWORD); PreparedStatement ps = con.prepareStatement(sql)) {

                ps.setInt(1, currentSelectedStaff.getStaffNumber());

                int rowsDeleted = ps.executeUpdate();

                if (rowsDeleted > 0) {
                    JOptionPane.showMessageDialog(this, "Staff deleted successfully.");

                    // refresh page
                    jButtonCleanActionPerformed(null);
                    formWindowOpened(null);
                    jButtonCancelActionPerformed(null);
                }

            } catch (SQLException e) {
                showError("Error deleting from database: " + e.getMessage());
            }
        }

        //Tool Tips off
        ToolTipsNull();
    }//GEN-LAST:event_jButtonDeleteActionPerformed

