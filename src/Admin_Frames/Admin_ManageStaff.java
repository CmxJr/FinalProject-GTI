    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        
        // Clear the list to avoid duplication on refresh
        jComboBoxStaffList.removeAllItems();
        jButtonSearch.setEnabled(true);

        String sql = "SELECT staff_id, first_name, middle_name, surname FROM staff ORDER BY first_name ASC";

        try (Connection con = DriverManager.getConnection(URL, USER, PASSWORD); PreparedStatement ps = con.prepareStatement(sql); ResultSet res = ps.executeQuery()) {

            while (res.next()) {
                
                // Instantiate Staff object using data from the database
                Staff s = new Staff();
                s.setStaffNumber(res.getInt("staff_id"));
                s.setFirstName(res.getString("first_name"));
                s.setMiddleName(res.getString("middle_name"));
                s.setSurname(res.getString("surname"));

                // Add the object to the ComboBox. 
                // The JComboBox will display the result of s.toString()
                jComboBoxStaffList.addItem(s);
            }

            // Set selection to empty by default
            jComboBoxStaffList.setSelectedIndex(-1);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading staff list: " + e.getMessage());
        }

    }//GEN-LAST:event_formWindowOpened

