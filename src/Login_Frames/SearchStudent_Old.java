    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        jLabelStudentName.setVisible(false);
        jLabelCourseCode.setVisible(false);
        jLabelCourse.setVisible(false);
        jButtonClean.setEnabled(false);
        jButtonSeeResults.setEnabled(false);

        jComboBoxSelectStudent.removeAllItems();

        String sql = "SELECT student_id, first_name, middle_name FROM student ORDER BY first_name, middle_name";

        try (Connection con = DriverManager.getConnection(URL, USER, PASSWORD); PreparedStatement ps = con.prepareStatement(sql); ResultSet res = ps.executeQuery()) {

         

            while (res.next()) {
                int id = res.getInt("student_id");
                String first = res.getString("first_name");
                String middle = res.getString("middle_name");

                String name
                        = (first == null ? "" : first) + " "
                        + (middle == null ? "" : middle);

                name = name.trim().replaceAll("\\s+", " ");

                jComboBoxSelectStudent.addItem(id + " : " + name);
            }

            jComboBoxSelectStudent.setSelectedIndex(0);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
        jComboBoxSelectStudent.setSelectedIndex(-1);


    }//GEN-LAST:event_formWindowActivated

