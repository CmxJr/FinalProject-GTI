    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        String SelectedStudent = jComboBoxSelectStudent.getSelectedItem().toString();

         if (SelectedStudent == null) {
        JOptionPane.showMessageDialog(this, "Please select a student.");
        return;
    }

    // Pega tudo antes do ":"
    String idPart = SelectedStudent.split(":")[0].trim();
    int studentId;

    try {
        studentId = Integer.parseInt(idPart);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid student selection.");
        return;
    }

    String sql = """
        SELECT s.class_group_code, s.student_id, s.first_name, s.middle_name, s.surname, c.course_name
        FROM student s
        JOIN class_group cg ON s.class_group_code = cg.class_group_code
        JOIN course c ON cg.course_code = c.course_code
        WHERE s.student_id = ?
    """;

    try (Connection con = DriverManager.getConnection(URL, USER, PASSWORD);
         PreparedStatement ps = con.prepareStatement(sql)) {

        ps.setInt(1, studentId);

        try (ResultSet res = ps.executeQuery()) {
            if (res.next()) {
                String fullName =
                        (res.getString("first_name") + " " +
                         res.getString("middle_name") + " " +
                         res.getString("surname"))
                         .trim().replaceAll("\\s+", " ");

                int courseId = res.getInt("class_group_code");
                String courseName = res.getString("course_name");

                jLabelStudentName.setText("Student name: " + fullName);
                jLabelCourseCode.setText("Course code: " + courseId);
                jLabelCourse.setText("Course: " + courseName);

                selectedStudentId = res.getInt("student_id");

                jLabelStudentName.setVisible(true);
                jLabelCourseCode.setVisible(true);
                jLabelCourse.setVisible(true);

                jButtonSeeResults.setEnabled(true);
                jButtonClean.setEnabled(true);
                jButtonSeeResults.requestFocus();
            } else {
                JOptionPane.showMessageDialog(this, "Student not found.");
            }
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
    }
        jLabelStudentName.setVisible(true);
        jLabelCourse.setVisible(true);
        jLabelCourseCode.setVisible(true);
        jButtonSeeResults.setEnabled(true);
        jButtonClean.setEnabled(true);
        jButtonSeeResults.requestFocus(true);
    }//GEN-LAST:event_jButtonSearchActionPerformed

