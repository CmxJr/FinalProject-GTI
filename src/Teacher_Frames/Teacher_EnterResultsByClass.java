    private void jButtonSearchCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchCourseActionPerformed
        // Enable UI components before processing
        jButtonSearchClass.setEnabled(true);
        jComboBoxClass.setEnabled(true);
        jComboBoxModule.removeAllItems();

        // Clear the combo box to avoid duplicating items from previous searches
        jComboBoxClass.removeAllItems();

        // Get the selected item from the Course combo box
        Object selectedItem = jComboBoxCourse.getSelectedItem();

        if (selectedItem == null || !selectedItem.toString().contains(":")) {
            JOptionPane.showMessageDialog(this, "Please select a  course first.");
            return;
        }

        String selectedCourse = selectedItem.toString();

        try {
            // Parsing the ID from a string format like "1 : Computer Science"
            String selectedText = selectedCourse.trim();
            int colonIdx = selectedText.indexOf(':');
            selectedCourseID = Integer.parseInt(selectedText.substring(0, colonIdx).trim());

            String sql = "SELECT class_group_code FROM class_group WHERE course_id = ?";

            try (Connection con = DriverManager.getConnection(URL, USER, PASSWORD); PreparedStatement ps = con.prepareStatement(sql)) {

                // Bind the selected ID to the first parameter (?) of the query
                ps.setInt(1, selectedCourseID);

                // Execute the query and process the results
                try (ResultSet rs = ps.executeQuery()) {
                    boolean classFound = false;

                    while (rs.next()) {
                        // Extract the class code from the current row
                        int classId = rs.getInt("class_group_code");

                        // Add the result to the combo box
                        jComboBoxClass.addItem("Class: " + classId);
                        classFound = true;
                    }

                    // Optional: notify user if no classes are linked to this course
                    if (!classFound) {
                        JOptionPane.showMessageDialog(this, "No classes found for the selected course.");
                    }
                }
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Error: Invalid course ID format.");
        } catch (SQLException e) {
            // Handle database connection or syntax errors
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }


    }//GEN-LAST:event_jButtonSearchCourseActionPerformed

    private void jButtonSearchClassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchClassActionPerformed
        jComboBoxModule.removeAll();
        jButtonSearchCourse.setEnabled(false);
        jButtonSearchClass.setEnabled(false);
        jComboBoxCourse.setEnabled(false);
        jComboBoxClass.setEnabled(false);

        Object selectedClass = jComboBoxClass.getSelectedItem();

        String selectedText = selectedClass.toString().trim();
        String numericOnly = selectedText.replaceAll("[^0-9]", "");
        selectedClassID = Integer.parseInt(numericOnly);

        try {

            String sql = """
        SELECT DISTINCT m.module_name
        FROM class_group cg
        JOIN course_has_module chm ON cg.course_id = chm.course_id
        JOIN module m ON chm.qqi_module_code = m.qqi_module_code
        WHERE cg.class_group_code = ?
    """;

            try (Connection con = DriverManager.getConnection(URL, USER, PASSWORD); PreparedStatement ps = con.prepareStatement(sql)) {

                // Set the Class Group ID parameter
                ps.setInt(1, selectedClassID);

                try (ResultSet rs = ps.executeQuery()) {
                    boolean hasModules = false;

                    while (rs.next()) {
                        // Fetch only the name column
                        String moduleName = rs.getString("module_name");

                        // Add the name directly to the UI component
                        jComboBoxModule.addItem(moduleName);
                        hasModules = true;
                    }

                    if (!hasModules) {
                        JOptionPane.showMessageDialog(this, "No modules associated with this class group.");
                    }
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        }

        jComboBoxModule.setEnabled(true);
        jButtonSearchModule.setEnabled(true);

    }//GEN-LAST:event_jButtonSearchClassActionPerformed

