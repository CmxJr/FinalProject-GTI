    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        // Get the table model and clear existing rows
        var model = (javax.swing.table.DefaultTableModel) jTableResults.getModel();
        model.setRowCount(0);

        // Get the selected object from the ComboBox
        Object selectedItem = jComboBoxSelectStudent.getSelectedItem();

        // Validation: Check if something is selected and if it's an instance of Student class
        if (selectedItem == null || !(selectedItem instanceof Student)) {
            JOptionPane.showMessageDialog(this, "Please select a valid student from the list.");
            return;
        }

        // Cast the selected item to Student object to access its methods
        Student selectedStudent = (Student) selectedItem;
        int studentId = selectedStudent.getStudentId();
        this.selectedStudentId = studentId; // Update global variable

       // Prepare SQL Query
        String sql = """
        SELECT 
        s.first_name,
        s.middle_name,
        s.surname, 
        c.course_name, 
        m.module_name, 
        a.assignment_1, 
        a.assignment_2, 
        a.exam, 
        a.result
        FROM student s
        JOIN class_group cg        ON s.class_group_code = cg.class_group_code
        JOIN course c             ON cg.course_id = c.course_id
        JOIN course_has_module chm ON c.course_id = chm.course_id 
        JOIN module m             ON chm.qqi_module_code = m.qqi_module_code
        LEFT JOIN assessment a    ON (
        a.student_id = s.student_id AND 
        a.qqi_module_code = m.qqi_module_code
        )
        WHERE s.student_id = ?
        ORDER BY m.module_name ASC;
        """;

        // Establish database connection and execute prepared statement
        try (Connection con = DriverManager.getConnection(URL, USER, PASSWORD); PreparedStatement ps = con.prepareStatement(sql)) {

            ps.setInt(1, studentId);

            try (ResultSet rs = ps.executeQuery()) {
                boolean dataFound = false;

                while (rs.next()) {
                    // Update UI labels only once using data from the first row
                    if (!dataFound) {
                        String fullName = rs.getString("first_name") + " " + rs.getString("middle_name") + " " + rs.getString("surname");
                        jLabelStudentName.setText("Student: " + fullName);
                        jLabelCourse.setText("Course: " + rs.getString("course_name"));
                        dataFound = true;
                    }

                    // Retrieve module and grades (handling potential nulls from LEFT JOIN)
                    String sqlmoduleName = rs.getString("module_name");
                    Object sqla1 = rs.getObject("assignment_1");
                    Object sqla2 = rs.getObject("assignment_2");
                    Object sqlexam = rs.getObject("exam");
                    Object sqlresult = rs.getObject("result");

                    // Add formatted row to the JTable
                    model.addRow(new Object[]{
                        sqlmoduleName,
                        sqla1,
                        sqla2,
                        sqlexam,
                        sqlresult
                    });
                }

                // Inform user if no records were returned for the specific ID
                if (!dataFound) {
                    JOptionPane.showMessageDialog(this, "No records found for the selected student.");
                }
            }
        } catch (SQLException e) {
            // Log and display database-related errors
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
            logger.log(java.util.logging.Level.SEVERE, "Database error occurred", e);
        }
    }//GEN-LAST:event_jButtonSearchActionPerformed

    private void jButtonEnterResultsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnterResultsActionPerformed
        // Force the table to stop editing to capture the last typed value
        if (jTableResults.isEditing()) {
            jTableResults.getCellEditor().stopCellEditing();
        }

        // Security check: make sure a student was loaded
        if (selectedStudentId == 0) {
            JOptionPane.showMessageDialog(this, "Please select a student and search first.");
            return;
        }

        // RUN VALIDATION
        if (!validateGrades()) {
            return;
        }

        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTableResults.getModel();
        int rowCount = model.getRowCount();

        // Simplified SQL using the hidden ID
        String sqlUpdate = """
        UPDATE assessment a 
        JOIN module m ON a.qqi_module_code = m.qqi_module_code
        SET a.assignment_1 = ?, a.assignment_2 = ?, a.exam = ?, a.result = ?
        WHERE a.student_id = ? AND m.module_name = ?
        """;

        try (Connection con = DriverManager.getConnection(URL, USER, PASSWORD); PreparedStatement ps = con.prepareStatement(sqlUpdate)) {

            for (int i = 0; i < rowCount; i++) {
                moduleName = model.getValueAt(i, 0).toString();

                // Grades already validated by validateGrades
                A1 = Double.parseDouble(model.getValueAt(i, 1).toString());
                A2 = Double.parseDouble(model.getValueAt(i, 2).toString());
                exam = Double.parseDouble(model.getValueAt(i, 3).toString());

                double result = Math.round(((A1 + A2 + exam) / 3.0) * 100.0) / 100.0;

                // Confirm save for each row (or you can move this outside the loop for a single confirmation)
                String message = "Save grades for: " + moduleName + "?\n\n"
                        + "Assignment 1: " + A1 + "\n"
                        + "Assignment 2: " + A2 + "\n"
                        + "Exam: " + exam + "\n\n"
                        + "FINAL RESULT: " + String.format("%.2f", result);

                int choice = JOptionPane.showConfirmDialog(null,
                        message,
                        "Confirm Save",
                        JOptionPane.YES_NO_CANCEL_OPTION);

                if (choice == JOptionPane.YES_OPTION) {
                    ps.setDouble(1, A1);
                    ps.setDouble(2, A2);
                    ps.setDouble(3, exam);
                    ps.setDouble(4, result);
                    ps.setInt(5, selectedStudentId);
                    ps.setString(6, moduleName);

                    ps.executeUpdate();
                } else if (choice == JOptionPane.CANCEL_OPTION) {
                    // Stop the process for the rest of the class
                    break;
                }
            }
            JOptionPane.showMessageDialog(this, "Process finished.");

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
            logger.log(java.util.logging.Level.SEVERE, "Database error occurred", e);
        }

        UpdateSearch(); // Refresh the table with new results


    }//GEN-LAST:event_jButtonEnterResultsActionPerformed

