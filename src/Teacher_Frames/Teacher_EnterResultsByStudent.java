    private void printToPDF() {
        // 1. basic validation
        if (jTableResults.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Search for a student and results first!");
            return;
        }

        // 2. " Save as" windows
        javax.swing.JFileChooser chooser = new javax.swing.JFileChooser();
        chooser.setDialogTitle("Save Report");
        int Year = LocalDate.now().getYear(); //This year
        chooser.setSelectedFile(new java.io.File("REPORT_ACADEMIC_RECORD_" + Year + "_" + jLabelStudentName.getText() + ".pdf"));

        if (chooser.showSaveDialog(this) == javax.swing.JFileChooser.APPROVE_OPTION) {
            String path = chooser.getSelectedFile().getAbsolutePath();
            if (!path.endsWith(".pdf")) {
                path += ".pdf";
            }

            try {
                com.itextpdf.text.Document document = new com.itextpdf.text.Document();
                com.itextpdf.text.pdf.PdfWriter.getInstance(document, new java.io.FileOutputStream(path));

                document.open();

                // Style and content
                document.add(new com.itextpdf.text.Paragraph("STUDENT ACADEMIC RECORD " + Year));
                document.add(new com.itextpdf.text.Paragraph(jLabelStudentName.getText()));
                document.add(new com.itextpdf.text.Paragraph(jLabelCourse.getText()));
                document.add(new com.itextpdf.text.Paragraph(" ")); // Space

                // New table nn PDF
                var model = (javax.swing.table.DefaultTableModel) jTableResults.getModel();
                com.itextpdf.text.pdf.PdfPTable pdfTable = new com.itextpdf.text.pdf.PdfPTable(model.getColumnCount());

                // Heads
                for (int i = 0; i < model.getColumnCount(); i++) {
                    pdfTable.addCell(model.getColumnName(i));
                }

                // Data
                for (int r = 0; r < model.getRowCount(); r++) {
                    for (int c = 0; c < model.getColumnCount(); c++) {
                        Object value = model.getValueAt(r, c);
                        pdfTable.addCell(value == null ? "-" : value.toString());
                    }
                }

                document.add(pdfTable);
                document.close();

                JOptionPane.showMessageDialog(this, "PDF saved successfully!");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error generating PDF: " + e.getMessage());
            }
        }
    }
    
    private boolean validateGrades() {
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTableResults.getModel();
    int emptyFieldsCount = 0; // Counter for empty fields

    for (int i = 0; i < model.getRowCount(); i++) {
        Object modValue = model.getValueAt(i, 0);
        String modName = (modValue == null) ? "Unknown" : modValue.toString();

        for (int col = 1; col <= 3; col++) {
            Object value = model.getValueAt(i, col);
            String colName = jTableResults.getColumnName(col);
            double grade;

            // 1. Check for NULL or EMPTY fields
            if (value == null || value.toString().trim().isEmpty()) {
                grade = 0.0;
                model.setValueAt(grade, i, col); 
                emptyFieldsCount++; // Increment counter
            } else {
                try {
                    String rawValue = value.toString().trim().replace(",", ".");
                    grade = Double.parseDouble(rawValue);

                    if (grade < 0 || grade > 10) {
                        JOptionPane.showMessageDialog(this,
                                "Invalid grade in module: " + modName + "!\n"
                                + "Field: " + colName + "\n"
                                + "Value: " + grade + "\n"
                                + "Grades must be between 0.00 and 10.00.");
                        return false;
                    }
                    model.setValueAt(grade, i, col);
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this,
                            "Invalid format in module: " + modName + " (" + colName + ").\n"
                            + "Please enter only numbers or leave it blank for 0.00.");
                    return false;
                }
            }
        }
    }

    // 2. SHOW ALERT IF EMPTY FIELDS WERE FOUND
    if (emptyFieldsCount > 0) {
        int response = JOptionPane.showConfirmDialog(this,
                "Warning: " + emptyFieldsCount + " empty field(s) detected.\n"
                + "They have been automatically set to 0.00.\n"
                + "Do you want to proceed with these values?",
                "Empty Fields Detected",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);
        
        if (response == JOptionPane.NO_OPTION) {
            return false; // Stop the process so the teacher can edit
        }
    }

    return true;
}

